# 5 > 4 : Maior que 4
print("5 Maior que 4 é: ", 5 > 4)

# 4 < 5 : Menor que 5
print("4 Menor que 5 é: ", 4 < 5)

# 6 != 6 : aqui eu quero saber se 6 é diferente de 6
print("6 diferente de 6 é: ", 6 != 6)

# 100 >= 101 : 100 é maior ou igual a 101, aqui ele deve dizer False
