### Variaveis, valores que podem receber dados que serão armazenados na memoria do computador.

### Variavel Numerico do tipo inteiro
idade = 19

### Variavel Numerico do tipo real ou fracionado
salario = 960.50

### Variavel do tipo Caractere
nome = "Astra"

### Variavel do tipo booleano ou Lógico
solteiro = True

### Imprimir o tipo das variaveis seleiconadas
print(type(idade), type(nome), type(salario), type(solteiro))